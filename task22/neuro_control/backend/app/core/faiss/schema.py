# app/core/faiss/schema.py

"""
Схемы Pydantic для работы с модулем FAISS.

Этот файл содержит модели запросов и ответов для FastAPI роутеров
FAISS, обеспечивая валидацию данных на входе и выходе:

1. FaissBuildRequest       — параметры для создания или пересоздания индекса
2. FaissSearchRequest      — параметры для поиска по индексу
3. FaissSearchResponseItem — один результат поиска
4. FaissSearchResponse     — полный список результатов поиска

Используется в роутерах:
- /faiss/build
- /faiss/rebuild
- /faiss/search
"""

from pydantic import BaseModel
from typing import Optional, List

# ==========================================================
# Запросы на создание или пересоздание индекса
# ==========================================================
class FaissBuildRequest(BaseModel):
    """
    Параметры запроса на создание или пересоздание FAISS индекса.

    Поля:
    - text_file (str): путь к файлу базы знаний.
    - chunk_size (Optional[int]): размер чанка для разбиения текста
      при построении индекса. По умолчанию 1000 символов.
    """
    text_file: str
    chunk_size: Optional[int] = 1000


# ==========================================================
# Запросы на поиск по существующему индексу
# ==========================================================
class FaissSearchRequest(BaseModel):
    """
    Параметры запроса на поиск по FAISS индексу.

    Поля:
    - text_file (str): путь к файлу, на основе которого построен индекс.
      Если индекс еще не создан — он будет автоматически построен.
    - query (str): поисковый запрос.
    - k (Optional[int]): количество релевантных результатов для возврата.
      По умолчанию 5.
    """
    text_file: str
    query: str
    k: Optional[int] = 5


# ==========================================================
# Результат одного найденного чанка
# ==========================================================
class FaissSearchResponseItem(BaseModel):
    """
    Модель для одного результата поиска по индексу.

    Поля:
    - content (str): текст найденного чанка.
    - score (float): оценка релевантности. Чем меньше, тем ближе к запросу.
    """
    content: str
    score: float


# ==========================================================
# Ответ на поиск по FAISS индексу
# ==========================================================
class FaissSearchResponse(BaseModel):
    """
    Модель ответа на поиск по FAISS индексу.

    Поля:
    - results (List[FaissSearchResponseItem]): список найденных чанков,
      отсортированных по релевантности (от наиболее релевантного к менее).
    """
    results: List[FaissSearchResponseItem]