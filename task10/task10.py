def chat_with_memory(client, history, system):
    # –°–æ–∑–¥–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –∫–æ—Ç–æ—Ä–æ–∞—è —Å–æ–¥–µ—Ä–∂–∏—Ç –≤—Å—é –∏—Å—Ç–æ—Ä–∏—é –¥–∏–∞–ª–æ–≥–∞
    full_history = "\n".join(history)

    # –ó–∞–ø—Ä–æ—Å –∫ GPT —Å –∏—Å—Ç–æ—Ä–∏–µ–π
    try:
        completion = client.chat.completions.create(
            model="gpt-4.1",
            messages=[
                {"role": "system", "content": system},
                {"role": "user", "content": f"""–í–µ–¥–∏ –∏–Ω—Ç–µ—Ä–µ—Å–Ω—ã–π, —á–µ—Ç–∫–∏–π –∏ –ø–æ–Ω—è—Ç–Ω—ã–π —Ä–∞–∑–≥–æ–≤–æ—Ä —Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º. –í–æ—Ç –∏—Å—Ç–æ—Ä–∏—è –≤–∞—à–µ–≥–æ –¥–∏–∞–ª–æ–≥–∞: {full_history}. –ï—Å–ª–∏ –ö–ª–∏–µ–Ω—Ç –Ω–∞–ø–∏—à–µ—Ç —Ç–µ–±–µ —Å–≤–æ–∏
                –∫–æ–Ω—Ç–∞–∫—Ç—ã, –ø–æ–±–ª–∞–≥–æ–¥–∞—Ä–∏ –µ–≥–æ –∏ –Ω–∞–ø–æ–º–Ω–∏, —á—Ç–æ —Å –Ω–∏–º –æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ —Å–≤—è–∂—É—Ç—Å—è. –ï—Å–ª–∏ –∫–ª–∏–µ–Ω—Ç –ø–æ–±–ª–∞–≥–æ–¥–∞—Ä–∏–ª –∏ –ø–æ–ø—Ä–æ—â–∞–ª—Å—è –∏–ª–∏ –ø—Ä–æ—Å—Ç–æ –ø–æ–ø—Ä–æ—â–∞–ª—Å—è, –æ—Ç–≤–µ—Ç—å –∫—Ä–∞—Ç–∫–æ: "–î–æ —Å–≤–∏–¥–∞–Ω–∏—è! –í—Å–µ–≥–æ –¥–æ–±—Ä–æ–≥–æ!" –∏ –±–æ–ª—å—à–µ –Ω–∏—á–µ–≥–æ –Ω–µ –ø–∏—à–∏"""}
            ],
            temperature=0.15,
        )

        # –ü–æ–ª—É—á–∞–µ–º –æ—Ç–≤–µ—Ç GPT –∏ –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –≤ –∏—Å—Ç–æ—Ä–∏—é
        if completion and completion.choices:
            answer = completion.choices[0].message.content
            history.append(f"GPT: {answer}")
            return answer
        else:
            answer = "–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ –æ—Ç–≤–µ—Ç–∞ –æ—Ç GPT."

    except Exception as e:
        answer = f"–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞: {e}"

